package com.class3;

public class Class3Task {
	

		

	
	public static void main(String[] args) {

		double a = 5.5;
		double b = 2.5;

		if (a >= b) {
			System.out.println("Double value " + a + " is larger than " + b + ".");

		} else {
			System.out.println("Double value " + a + " is smaller than " + b + ".");

		}

		double c = 1.5;
		double d = 2.5;

		if (c <= d) {
			System.out.println("Double value " + c + " is smaller than " + d + ".");

		} else {
			System.out.println("Double value " + c + " is larger than " + d + ".");
		}

	}

}



