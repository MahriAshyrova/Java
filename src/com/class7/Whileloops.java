package com.class7;

public class Whileloops {

	public static void main(String[] args) {
		// to eliminate repetition we use loops
		
	int a=1;
	
	while (a <10)	{
		
		System.out.println("Good morning");
		a++;
	}

	System.out.println();
	
	int b=1;
	
	while (b<=5) {
		System.out.println("I am inside the while loop "+b);
		b++;
	}
	System.out.println("I am outside of the while loop");
	
	System.out.println();
	
	
	//i want to print values from 10-20 inclusive
	
	int z=10;
	
	while (z<=20) {
		
		System.out.println(z);
		z++;
	}
	
	System.out.println();
	
	//i want to print values from 1-20 inclusive all in one line and with a space;
	
	
	int q= 1;
	
	while (q<=20) {
	System.out.print(q+ " ");
	q++;
	}
	
	System.out.println();
	
	//i want to print values from 10-1 inclusive all in one line and with a space;
	
	int e= 10;
	
	while (e>=1) {
		System.out.println(e);
		e--;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}

}
