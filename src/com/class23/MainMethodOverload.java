package com.class23;

public class MainMethodOverload {

	public static void main(String[] args) {
		
		System.out.println("I am a main method with String argument array");
		
	
		
	}

	public static void main() {
		System.out.println("I am a main method with no parameters");

	}
	
	public static void main(String args) {
		System.out.println("I am a main method with a String parameter");

	}
	
	public static void main(int a) {
		System.out.println("I am a main method with an integer parameter");

	}
	
}
