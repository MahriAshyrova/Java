package com.class2;

public class VariablesDeclaration {

	public static void main(String[] args) {
		// different ways to create/declare variables;
		// 1. create variables and assign a value in the same line

		int num = 28;

		// 2ways: using 2 steps
		int y; // 1. declaring variables
		y = 12; // 2. assign a value

		// declare one by one
		int a;
		int b;
		int c;
		int d;
		int e;

		// or declare all together
		int f, g, h, i, j;
		a = 10;
		b = 20;

		System.out.println(num);

		// for Mac usesrs cmd+shift+f = formating 

	}

}
